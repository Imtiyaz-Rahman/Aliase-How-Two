# Git Aliases

alias gs='git status'                    # Show working tree status
alias gaa='git add --all'                # Stage all changes
alias gcm='git commit -m'                # Commit with message
alias gp='git push'                      # Push to remote
alias gpl='git pull'                     # Pull from remote
alias gco='git checkout'                 # Switch branches
alias gcb='git checkout -b'              # Create and switch to new branch
alias gb='git branch'                    # List branches
alias gbd='git branch -d'                # Delete branch
alias gst='git stash'                    # Stash changes
alias gstp='git stash pop'               # Apply stashed changes
alias gm='git merge'                     # Merge branches
alias grh='git reset --hard'             # Discard all changes

